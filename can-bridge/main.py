async def sim_loop():
    """Generate simulated CAN data for testing without hardware."""
    print("[SIM] Running in simulation mode — no serial device detected")
    ids = list(SIM_FRAMES.keys())
    i = 0
    while True:
        can_id = ids[i % len(ids)]
        data = bytearray(SIM_FRAMES[can_id])

        # Add slight variation to RPM and speed on each pass
        if can_id == 0x0B6:
            rpm = 800 + random.randint(-50, 100)
            speed = 50 + random.randint(-5, 15)
            data[0] = (rpm >> 8) & 0xFF
            data[1] = rpm & 0xFF
            data[2] = 0
            data[3] = speed
        if can_id == 0x0F6:
            data[1] = 85 + random.randint(-3, 5)  # coolant temp variation

        parsed = parse_frame(can_id, bytes(data))
        msg = {
            "id": f"0x{can_id:03X}",
            "ts": time.time(),
            "data": parsed,
            "raw": " ".join(f"{b:02X}" for b in data[:8])
        }
        asyncio.create_task(broadcast(json.dumps(msg)))
        i += 1
        await asyncio.sleep(0.15)  # ~6-7 frames/sec feels responsive


async def can_loop():
    """Read CAN frames from serial and broadcast to WebSocket clients."""
    reader.open()
    loop = asyncio.get_event_loop()
    while True:
        frame = await loop.run_in_executor(None, reader.read_frame)
        if frame is not None:
            parsed = parse_frame(frame.can_id, frame.data)
            msg = {
                "id": f"0x{frame.can_id:03X}",
                "ts": time.time(),
                "data": parsed,
                "raw": " ".join(f"{b:02X}" for b in frame.data[:frame.dlen])
            }
            asyncio.create_task(broadcast(json.dumps(msg)))
        await asyncio.sleep(0.001)


@app.on_event("startup")
async def startup():
    if SIMULATION:
        asyncio.create_task(sim_loop())
        print(f"[SIM] Simulation mode active on http://localhost:8765")
    else:
        asyncio.create_task(can_loop())


