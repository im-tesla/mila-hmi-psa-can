@app.websocket("/ws")
async def websocket_endpoint(ws: WebSocket):
    await ws.accept()
    clients.append(ws)
    try:
        while True:
            await ws.receive_text()
    except WebSocketDisconnect:
        clients.remove(ws)


async def broadcast(frame_json: str):
    for ws in clients[:]:
        try:
            await ws.send_text(frame_json)
        except Exception:
            clients.remove(ws)


